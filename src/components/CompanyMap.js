  afterAnimation() {
    if (this.state.mouseHold) {
      document.getElementById("mapContainer").style.cursor = "context-menu";
      this.setState({ mouseHold: false });
    }

    let mouseVelocity = JSON.parse(JSON.stringify(this.state.mouseVelocity));
    let center = JSON.parse(JSON.stringify(this.state.center));
    var t = this;
    this.interval = setInterval(function () {
      mouseVelocity.x *= 0.95;
      mouseVelocity.y *= 0.95;

      if (mouseVelocity.x <= 0.5 && mouseVelocity.y <= 0.5) {
        mouseVelocity.x = 0;
        mouseVelocity.y = 0;
      }

      center.x += mouseVelocity.x * 0.3;
      center.y += mouseVelocity.y * 0.3;

      if (center.x <= -1150) {
        center.x = -1150;
      } else if (center.x >= 1150) {
        center.x = 1150;
      }

      if (center.y <= -800) {
        center.y = -800;
      } else if (center.y >= 800) {
        center.y = 800;
      }

      document.getElementById(
        "plan4"
      ).style.transform = `translate(${center.x}px, ${center.y}px)`;
      document.getElementById(
        "plan5"
      ).style.transform = `translate(${center.x}px, ${center.y}px)`;

      t.movementCache = { x: center.x, y: center.y };

      if (mouseVelocity.x === 0 && mouseVelocity.y === 0) {
        clearInterval(this.interval);
        t.movementCache = null;
      }
    }, 10);
  }

  move(e) {
    if (this.state.mouseHold) {
      document.getElementById("mapContainer").style.cursor = "move";
      let mouseVelocity = JSON.parse(JSON.stringify(this.state.mouseVelocity));
      let center = JSON.parse(JSON.stringify(this.state.center));

      if (this.movementCache === null) {
        center.x += e.movementX * (1 / this.state.zoom);
        center.y += e.movementY * (1 / this.state.zoom);
      } else {
        center.x = this.movementCache.x;
        center.y = this.movementCache.y;
        // center.x = 0;
        // center.y = 0;
        this.movementCache = null;
      }

      if (center.x <= -1150) {
        center.x = -1150;
      } else if (center.x >= 1150) {
        center.x = 1150;
      }

      if (center.y <= -800) {
        center.y = -800;
      } else if (center.y >= 800) {
        center.y = 800;
      }

      mouseVelocity.x = center.x - this.state.center.x;
      mouseVelocity.y = center.y - this.state.center.y;

      this.setState({ center, mouseVelocity });
    } else {
      document.getElementById("mapContainer").style.cursor = "context-menu";
    }
  }

  zoom(e) {
    let zoom = this.state.zoom + e.deltaY * -0.15;
    let center = this.state.center;
    center.x += e.movementX * (1 / this.state.zoom);
    center.y += e.movementY * (1 / this.state.zoom);
    // let positionDeltaX = Math.abs(center.x - this.state.center.x);
    // let positionDeltaY = Math.abs(center.y - this.state.center.y);

    if (zoom <= 1) {
      zoom = 1;
    } else if (zoom >= 10) {
      zoom = 10;
    }

    // if(positionDeltaX >= 100) {
    // 	center.x = this.state.center.x + 100;

    // }
    // if(positionDeltaY >= 100) {
    // 	center.y = this.state.center.y + 100;
    // }

    this.setState({ zoom, center });
    // console.log(center);
  }

